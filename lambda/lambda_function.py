class GetWODIntentHandler(AbstractRequestHandler):
    """Handler for Hello World Intent."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_intent_name("GetWODIntent")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        when_slot_value = ask_utils.get_slot_value(handler_input, "when")
        
        if when_slot_value is not None:
            wod_requested = datetime.strptime(when_slot_value, "%Y-%m-%d").strftime("%y%m%d")
        else:
            wod_requested = datetime.now().strftime("%y%m%d")
        
        loop = asyncio.get_event_loop()
        wod_text = loop.run_until_complete(make_request(wod_requested))
        
        soup = BeautifulSoup(wod_text, 'html.parser')
        wod = soup.find(class_='_6zX5t4v71r1EQ1b1O0nO2 jYZW249J9cFebTPrzuIl0')
        wod_str = ','.join(wod.get_text(separator=', ', strip=True).split(',')[:-1])
            
        speak_output = f"The WOD is: {wod_str if wod_str.endswith('.') else wod_str + '.'} <amazon:emotion name=\"excited\" intensity=\"high\">Lets get it done!</amazon:emotion>"

        return (
            handler_input.response_builder
                .speak(speak_output)
                # .ask("add a reprompt if you want to keep the session open for the user to respond")
                .response
        )


